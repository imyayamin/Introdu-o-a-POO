#Crie um algoritmo que leia a base e a altura de um retângulo e calcule sua área. 
#O algoritmo deve mostrar o valor da área no final. Fórmula: area = base * altura.

base = float(input("Digite o valor da base do retangulo(em centimetros):"))

altura = float(input("Digite o valor da altura do retangulo(em centimetros):"))

area = base * altura

print("A area desse retangulo é de: ", area," centimetros")
