#Crie um algoritmo que leia o nome da pessoa, leia a idade atual e mostre quantos anos a pessoa terá daqui a 10 anos.

nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade atual: "))

idadeFutura = idade + 10

print("Seu nome é ",nome," atualmente tem ",idade," e daqui 10 anos terá", idadeFutura)
